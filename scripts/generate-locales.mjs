function deepMerge(base, overlay) {
  const result = { ...base };
  for (const key of Object.keys(overlay)) {
    if (overlay[key] && typeof overlay[key] === "object" && !Array.isArray(overlay[key])) {
      result[key] = deepMerge(base[key] || {}, overlay[key]);
    } else {
      result[key] = overlay[key];
    }
  }
  return result;
}

